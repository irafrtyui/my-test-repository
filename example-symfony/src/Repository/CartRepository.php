    public function GetCart(): array
    {
        $sql = 'select 
       cart.id,
       cart.name, 
       cart.email,
       cart.price,
       cart.created_at
        from cart
        ';
        $connection = $this->_em->getConnection();
        $stm = $connection->prepare($sql);
        return $stm->executeQuery()->fetchAllAssociative();
    }

